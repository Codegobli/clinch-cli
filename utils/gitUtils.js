const { execSync } = require("child_process");

// ============================================
// RUN COMMAND - Execute terminal commands
// ============================================
function runCommand(command) {
  try {
    return execSync(command, {
      cwd: process.cwd(),
      encoding: "utf8",
      stdio: "pipe", // Capture output
    }).trim();
  } catch (error) {
    return null;
  }
}

